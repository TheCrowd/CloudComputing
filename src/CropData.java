public class CropData {
    private String suburb="";
    private Double total_agriculture=0.0;
    private Double total_value_of_crop=0.0;
    private Double veggies_for_human=0.0;

    public String getSuburb() {
        return suburb;
    }

    public void setSuburb(String suburb) {
        this.suburb = suburb;
    }

    public Double getTotal_agriculture() {
        return total_agriculture;
    }

    public void setTotal_agriculture(Double total_agriculture) {
        this.total_agriculture = total_agriculture;
    }

    public Double getTotal_value_of_crop() {
        return total_value_of_crop;
    }

    public void setTotal_value_of_crop(Double total_value_of_crop) {
        this.total_value_of_crop = total_value_of_crop;
    }

    public Double getVeggies_for_human() {
        return veggies_for_human;
    }

    public void setVeggies_for_human(Double veggies_for_human) {
        this.veggies_for_human = veggies_for_human;
    }
}
